    <h2>Постановка на учет автомобиля в ГИБДД, занимает достаточно много времени, и отнимает много сил.</h2>

    <h3>Для того, что бы пройти Регистрацию автомобиля в ГИБДД нужно:</h3>

    <ol>
        <li>Оформить Диагностическую карту техосмотра;Регистрация автомобиля в ГИБДД Строгино</li>
        <li>Оформить Обязательный страховой полис  ОСАГО;</li>
        <li>Приехать на транспортном средстве в отделение ГИБДД для его регистрации;</li>
        <li>Занять очередь на подачу документов для постановки автомобиля на учет (получить талон в терминале);</li>
        <li>Оплатить гос пошлину;</li>
        <li>Сдать документы в окно приема (когда до вас по вашему талону дойдет очередь);</li>
        <li>Пройти осмотр вашего автомобиля сотрудником ГИБДД;</li>
        <li>Получить свидетельство о регистрации вашего ТС и номера на машину;</li>
        <li>По желанию, можно оформить страховой полис КАСКО.</li>
        <li>При постановке на учет в ГИБДД, присутствие регистрируемого автомобиля, для осмотра, обязательное.</li>
    </ol>

