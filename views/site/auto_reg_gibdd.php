    <h3>Регистрация автомобиля в ГИБДД. Необходимый пакет документов для физ лиц:</h3>

    <ol>
        <li>Паспорт гражданина РФ;</li>
        <li>Паспорт транспортного средства (ПТС) на регистрируемое Транспортное Средство (ТС);</li>
        <li>Документ удостоверяющий право собственности (договор купли-продажи ТС);</li>
        <li>Страховой полис ОСАГО. Срок страхового полиса ОСАГО для физических лиц должен быть не менее 3 месяцев. Оформляем страховку на месте, по приезду клиента;</li>
        <li>Квитанция об оплате госпошлины безналичным платежом (оригинал);</li>
        <li>В случае, если владелец транспортного средства не присутствует при Постановке на учет, нужна Доверенность.</li>
    </ol>

    <h3>Регистрация автомобиля в ГИБДД. Необходимый пакет документов для юр лиц:</h3>

    <ol>
        <li>Паспорт гражданина РФ;</li>
        <li>Cпорт транспортного средства (ПТС) на регистрируемое Транспортное Средство (ТС);</li>
        <li>Документ удостоверяющий право собственности (договор купли-продажи ТС);</li>
        <li>Страховой полис ОСАГО. Срок страхового полиса ОСАГО для юридического лица должен быть не менее 1 года.  Оформляем страховку на месте, по приезду клиента;</li>
        <li>Квитанция об оплате госпошлины безналичным платежом (оригинал);</li>
        <li>Выписка из ЕГРЮЛ;</li>
        <li>Доверенность на регистрационные действия.</li>
        <li><ЕГРЮЛ — Единый государственный реестр юридических лиц./li>
    </ol>

    <h3>Общие требования для снятия автомобиля с учета в ГИБДД:</h3>

    <ul>
        <li>У снимаемого с учета автомобиля не должно быть незаконно перебитых номеров двигателя и кузова;</li>
        <li>Согласие владельца автомобиля. Если владельцев несколько, то снять с учета автомобиль возможно только если согласны будут ВСЕ владельцы;</li>
        <li>У будущего владельца транспортного средства не должно быть неоплаченных штрафов;</li>
        <li>Снимаемый с учета автомобиль не должен находится под арестом или быть вещественным доказательством в уголовном деле.</li>
    </ul>

