    <h3>Внимание! Пройти медкомиссию, для получении водительского удостоверения, вы можете непосредственно в здании ГИБДД Строгино.</h3>

    <h3>Обязательным условием допуска к управлению транспортным средством является медицинская справка установленного образца — форма 083/у-89. Для её получения необходимо пройти медкомиссию.</h3>

    <h3>Медицинская справка для ГИБДД необходима:</h3>

    <ul>
        <li>Если вы впервые сдаете экзамен на право управления транспортным средством, любой категории;</li>
        <li>При замене водительского удостоверения, по истечению его срока действий;</li>
        <li>При возврате ранее изъятого судом водительского удостоверения с истекшим сроком действия;</li>
        <li>При получении водительского удостоверения международного образца;</li>
        <li>При восстановлении украденного или утерянного водительского удостоверения.</li>
        <li>Сотрудники ГИБДД не имеют права требовать, что бы вы проходили медкомиссию в определенной поликлинике (например на территории ГИБДД).</li>
    </ul>

    <h3>Пройти медосмотр для получения справки, вы можете в любом мед. учреждении, которое имеет право производить профмедосмотры, на ваше усмотрение.</h3>

    <h3>Но, если Вы уверенны в своем здоровье, мы рекомендуем проходить медосмотр по месту сдачи экзамена на получение водительского удостоверения. Это существенно сэкономит ваше время.</h3>

